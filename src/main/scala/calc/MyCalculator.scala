package calc

class MyCalculator {
  def add(first: Int, second: Int): Int = {
    first + second
  }
  def sub(first: Int, second: Int): Int = {
    first - second
  }
  def mul(first: Int, second: Int): Int = {
    first * second
  }
  def div(first: Int, second: Int): Float = {
    first / second
  }
}
